create table member (
	ID		VARCHAR2(12),
	NAME	VARCHAR2(10),
	PASS	VARCHAR2(12),
	EMAIL1	VARCHAR2(30),
	EMAIL2	VARCHAR2(30),
	JOINDATE DATE DEFAULT SYSDATE,
	CONSTRAINTS MEMBER_ID	PRIMARY KEY (ID)
);

create table member_detail (
	NAME	VARCHAR2(10),
	ID		VARCHAR2(12),
	PASS	VARCHAR2(12),
	TEL1	NUMBER,
	TEL2	NUMBER,
	TEL3	NUMBER,
	ZIP1	VARCHAR2(30),
	ZIP2	VARCHAR2(30),
	EMAIL1	VARCHAR2(30),
	EMAIL2	VARCHAR2(30),
);

create table zipcode(
	ZIPCODE VARCHAR2(7),
	SIDO	VARCHAR2(100),
	GUGUN	VARCHAR2(100),
	DONG	VARCHAR2(100),
	BUNJI	VARCHAR2(100),
	SEQ		NUMBER PRIMARY KEY
);


